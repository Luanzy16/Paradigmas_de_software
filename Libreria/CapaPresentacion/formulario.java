     /**
     * funcion que crea la vista de la tabla libros en la aplicacion y le pone los elementos de la tabla de la base de datos
     */
    public void listarlibro(){
        DefaultTableModel tabla = new DefaultTableModel();
        DataLibro libro = new DataLibro();
        ArrayList<DataLibro> lista2 = new ArrayList();
        lista2 = libro.ListaLibro();
        tabla.addColumn("Codigo");
        tabla.addColumn("Titulo");
        tabla.addColumn("Autor");
        tabla.addColumn("Precio");
        tabla.setRowCount(lista2.size());
        int i=0;
        
        for(DataLibro x: lista2){
            tabla.setValueAt(x.getCodigo(), i, 0);
            tabla.setValueAt(x.getTitulo(), i, 1);
            tabla.setValueAt(x.getAutor(), i, 2);
            tabla.setValueAt(x.getPrecio(), i, 3);
            i++;
        }
        this.tablaLibros.setModel(tabla);
    }
    
