    private void jBeliminarlibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBeliminarlibroActionPerformed
        // TODO add your handling code here:
         int rec = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar el libro? "+ this.jTcodigolibro.getText());
        
        if (rec == 0){
            DataLibro libro1 = new DataLibro();
            libro1.setCodigo(Integer.parseInt(this.jTcodigolibro.getText()));
            JOptionPane.showMessageDialog(null, libro1.EliminarLibro());
            listarlibro();
            JOptionPane.showConfirmDialog(null, "Articulo Eliminado");
        }
    }//GEN-LAST:event_jBeliminarlibroActionPerformed

